# Logical Operators
p = True
q = False

# Logical AND : The and operator returns True if both operands are True.
print("p and q:", p and q)
# Logical OR : The or operator returns True if at least one operand is True.
print("p or q:", p or q)

'''
Rule for OR
True or True   → True
True or False  → True
False or True  → True
False or False → False
'''

print("not p:", not p)      # Logical NOT
