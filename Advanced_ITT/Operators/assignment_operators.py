# Assigning initial value
x = 10
print("Initial x:", x)

# Using various assignment operators
x += 5   # Equivalent to x = x + 5
print("After x += 5:", x)

x -= 2   # Equivalent to x = x - 2
print("After x -= 2:", x)

x *= 3   # Equivalent to x = x * 3
print("After x *= 3:", x)

x /= 4   # Equivalent to x = x / 4
print("After x /= 4:", x)

x %= 3   # Equivalent to x = x % 3
print("After x %= 3:", x)

x = 10   # Reset value
x //= 3  # Equivalent to x = x // 3
print("After x //= 3:", x)

x **= 2  # Equivalent to x = x ** 2
print("After x **= 2:", x)

x = 10   # Reset value
x &= 7   # Bitwise AND
print("After x &= 7:", x)

x |= 4   # Bitwise OR
print("After x |= 4:", x)

x ^= 2   # Bitwise XOR
print("After x ^= 2:", x)

x >>= 1  # Right shift by 1
print("After x >>= 1:", x)

x <<= 2  # Left shift by 2
print("After x <<= 2:", x)
